# Множество из одного элемента
s_1 = {1}
print('Множество из одного элемента ->', s_1)

# Множество из 4-х элементов
s_2 = {'Иванов', 0.1, ('Иванов', 2), 567, }
print('Множество из 4-х элементов ->', s_2)

# Множество с одинаковыми значениями элементов
s_3 = {'a', 1, 1, 1, 1}
print('Слияние одинаковых элементов множества ->', s_3)