# Исходные множества
Days1 = {"Понедельник",  "Вторник", "Среда", "Четверг"}
Days2 = {"Понедельник",  "Вторник"}
Days3 = {"Понедельник", "Вторник", "Пятница"}
Days4 = {"Вторник", "Понедельник"}
print('    Исходные множества')
print('Days1 - >', Days1)
print('Days2 - >', Days2)
print('Days3 - >', Days3)
print('Days4 - >', Days4)
print('-'*30)

# Результаты сравнения
print('     Результаты сравнения')
print('Days2 == Days4 ->', Days2 == Days4)
print('Days1 == Days2 ->', Days1 == Days2)
print('Days1 > Days2 ->', Days1 > Days2)
print('Days1 < Days2 ->', Days1 < Days2)
print('Days1 >= Days2 ->', Days1 >= Days2)
print('Days1 <= Days2 ->', Days1 <= Days2)