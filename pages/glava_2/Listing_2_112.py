print('Начальные значения переменным x и y')
x = 10
y = 20
print('x =', x)
print('y =', y)

z = (x == 10)
print('x == 10 ->', z)

z = (x != 10)
print('x != 10 ->', z)

z = (x > y)
print('x > y ->', z)

z = (x < y)
print('x < y ->', z)

z = (x >= 10)
print('x >= 10 ->', z)

z = (y <= 20)
print('y <= 20 ->', z)

