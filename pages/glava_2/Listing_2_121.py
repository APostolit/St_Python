print('Эти данные будут иметь тип - кортежи')
print("tuple('ABC') ->", tuple('ABC'))
print('tuple([1, 2, 3]) ->', tuple([1, 2, 3]))
print("tuple({'Один', 'Два'}) ->", tuple({'Один', 'Два'}))
print("tuple({1: 'Один', 2: 'Два'}) ->", tuple({1: 'Один', 2: 'Два'}))
print("tuple({'Один': 1, 'Два': 2}) ->", tuple({'Один': 1, 'Два': 2}))
print("tuple(range(1, 4)) ->", tuple(range(1, 4)))