print('Эти данные будут иметь тип - списки')
print("list('ABC') ->", list('ABC'))
print('list((1, 2, 3)) ->', list((1, 2, 3)))
print("list({'Один', 'Два'}) ->", list({'Один', 'Два'}))
print("list({1: 'Один', 2: 'Два'}) ->", list({1: 'Один', 2: 'Два'}))
print("list({'Один': 1, 'Два': 2}) ->", list({'Один': 1, 'Два': 2}))
print("list(range(1, 5)) ->", list(range(1, 5)))