print('Эти данные будут иметь тип - множество')
print("set('ABC') ->", set('abc'))
print('set((1, 2, 3)) ->', {1, 2, 3})
print("set({1: 'Один', 2: 'Два'}) ->", {1: 'Один', 2: 'Два'})
print("set({'Один': 1, 'Два': 2}) ->", {'Один': 1, 'Два': 2})
print("set(range(1, 4)) ->", set(range(1, 4)))