# Формируем строку
str_1 = 'Коля, Витя, Маша'

# Выводим первое слово
print('Первое слово - str_1[0:4] ->', str_1[0:4])
# Можно и так
print('Первое слово - str_1[:4] ->', str_1[:4])
print('------------------------')

# Выводим второе слово
print('Второе слово - str_1[6:10] ->', str_1[6:10])
print('------------------------')

# Выводим третье слово (используем отрицательные индексы)
print('Третье слово -str_1[-4:] ->', str_1[-4:])