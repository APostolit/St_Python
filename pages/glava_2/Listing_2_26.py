# Формируем строки
str_1 = 'Первая строка '
str_2 = 'Вторая строка'
str_3 = '-'

# Сложение строк
print('Сложение строк ->', str_1 + str_2)

# Сложение строк
print('Умножение строки на 3 ->', str_1 * 3)

# Сложение строк комбинированным оператором
str_1 += 'Сложение строк комбинированным оператором "+="'
print('Сложение строк оператором "+=" ->', str_1)

# Умножением удобно выводить повторяющиеся символы
print('Умножение символа ->', str_3*30)