# Формируем простой словарь.
d_1 = {1: 'Один', 2: 'Два', 3: 'Три'}
print('Исходный словарь - >', d_1)

# Наличие ключа - 2
print("Есть ли в словаре ключ -2 ? ->", 2 in d_1)
print("Есть ли в словаре ключ -4 ? ->", 4 in d_1)
print("Отсутствует ли в словаре ключ -4 ? ->", 4 not in d_1)