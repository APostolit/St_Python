# Формируем словарь
d_1 = {'a': 1, 'b': [2, 3], 'c': 'Иванов А.В.'}
print("---- Исходный словарь (d_1) ----")
print(d_1)
print('-'*40)

# Создаем копию словаря
d_2 = d_1.copy()
print("---- Копия словаря (d_2) ----")
print(d_2)
print('-'*40)

# Очищаем словарь (метод вернет None)
print("Очищаем словарь (метод вернет None) ->", d_2.clear())
# Выведет пустой словарь {}.
print("Словарь d2 после очистки ->", d_2)