# Обычное присваивание
a = "Иванов"
b = a
c = b
print('Выражение: a = "Иванов", b = a, c = b')
print('Итог: a ->', a, ', b ->', b, ', c ->', c)
print('-'*40)

# Групповое присваивание
c = b = a = "Ok"
print('Выражение: c = b = a = "Ok"')
print('Итог: a ->', a, ', b ->', b, ', c ->', c)