print('Все числа в диапазоне от 0 до 5 - range(5)')
for i in range(5):
    print('i=', i)
print('-'*5)

print('Все числа в диапазоне от 2 до 5 - range(2, 5)')
for i in range(2, 5):
    print('i=', i)
print('-'*5)

print('Все числа в диапазоне от 3 до 16 с шагом 3 - range(3, 16, 3)')
for i in range(3, 16, 3):
    print('i=', i)