# Источник ключей и значений для словаря из вложенного кортежа
tup_1 = (('Один', 1), ('Два', 2), ('Три', 3))
print('Источник для словаря вложенный кортеж')
print('tup_1 ->', tup_1)

print('Словарь из выражения-генератора и вложенного кортежа')
d_5 = dict(((k, v) for k, v in tup_1))
print('d_5 ->', d_5)