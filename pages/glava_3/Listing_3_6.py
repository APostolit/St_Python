# Присвоили начальное значение
a = 3
print('a = 3 ->', a)

a += 6  # Это равнозначно a = a + 6
print('a += 6 ->', a)

a *= 2  # Это равнозначно a = a*2
print('a *= 2 ->', a)

a /= 2  # Это равнозначно a = a/2
print('a /= 2 ->', a)

a **= 2  # Это равнозначно a = a**2
print('a **= 2 ->', a)

a %= 2  # Это равнозначно a = a%2
print('a %= 2 ->', a)