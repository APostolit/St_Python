# Рекурсивная функция
def sum_n(n):
    if n == 0:
        return n
    else:
        return n + sum_n(n - 1)

print('Сумма чисел - 0 ->', sum_n(0))
print('Сумма чисел от 0 до 1 ->', sum_n(1))
print('Сумма чисел от 0 до 2 ->', sum_n(2))
print('Сумма чисел от 0 до 3 ->', sum_n(3))
print('Сумма чисел от 0 до 10 ->', sum_n(10))
print('Сумма чисел от 0 до 20 ->', sum_n(20))